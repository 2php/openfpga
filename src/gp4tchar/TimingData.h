#ifndef TimingData_h
#define TimingData_h

//Delay values
class DelayPair
{
public:
	DelayPair(float r = 0, float f = 0)
	: rising(r)
	, falling(f)
	{  }

	float rising;
	float falling;
};

//Propagation delay through a cell (only one output supported for now)
class CellDelay
{
public:
	//map from pin name to delay
	std::map<std::string, DelayPair> delays;
};

#endif
